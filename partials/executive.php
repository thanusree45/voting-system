        <h2>About CSI</h2>
        <p>
            The Computer Society of India (CSI) is the first and the largest body of computer professionals in India. It was
            started on 6th March 1965 by a few computer professionals and has now grown to be the national body
            representing computer professionals.
        </p>

        <h2>Vision</h2>
<ul>
    <li>CSI is a non-profit professional society that meets to exchange views and information, learn and collaborate.</li>
    <li>The wide spectrum of members is committed to the advancement of the theory and practice of Computer Engineering and Technology, Systems, Science and Engineering, Information Processing, and related Arts and Sciences.</li>
</ul>

<h2>Mission</h2>
<ul>
    <li>Facilitate Research, Knowledge Sharing, Learning Enhancement for all categories of IT professionals.</li>
    <li>Inspire, nurture, and assist new entrants to integrate into the IT community.</li>
    <li>Percolation of IT advancement benefits to all Indians.</li>
</ul>

<h2>Executive Body</h2>
<p>
    The executive body of CSI comprises a President, a Vice President, a Secretary, a Treasurer, a Single Point of
    Contact (SPOC), and other members. <br>
    <ul>
        <li>The <b>President</b> leads the team</li>
        <li>The <b>Vice President</b> manages the team in the absence of the President</li>
        <li>The <b>Secretary</b> represents the team with eloquence</li>
        <li>The <b>Treasurer</b> manages financial matters</li>
        <li>The <b>SPOC</b> knows and solves problems in the student body or events.</li>
    </ul>
</p>

